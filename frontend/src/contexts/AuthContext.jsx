// Initial state
const initialState = {
  user: null,
  token: null,
  isAuthenticated: false,
  isLoading: true,
  isFirstLogin: false,
};

// Action types
const actionTypes = {
  AUTH_START: 'AUTH_START',
  AUTH_SUCCESS: 'AUTH_SUCCESS',
  AUTH_FAILURE: 'AUTH_FAILURE',
  LOGOUT: 'LOGOUT',
  UPDATE_USER: 'UPDATE_USER',
  SET_FIRST_LOGIN: 'SET_FIRST_LOGIN',
};

// Reducer
const authReducer = (state, action) => {
  switch (action.type) {
    case actionTypes.AUTH_START:
      return {
        ...state,
        isLoading: true,
      };
    case actionTypes.AUTH_SUCCESS:
      return {
        ...state,
        user: action.payload.user,
        token: action.payload.token,
        isAuthenticated: true,
        isLoading: false,
        isFirstLogin: action.payload.user?.isFirstLogin ?? action.payload.user?.is_first_login ?? false,
      };
    case actionTypes.AUTH_FAILURE:
      return {
        ...state,
        user: null,
        token: null,
        isAuthenticated: false,
        isLoading: false,
        isFirstLogin: false,
      };
    case actionTypes.LOGOUT:
      return {
        ...initialState,
        isLoading: false,
      };
    case actionTypes.UPDATE_USER:
      return {
        ...state,
        user: { ...state.user, ...action.payload },
        isFirstLogin: action.payload.isFirstLogin ?? action.payload.is_first_login ?? false,
      };
    case actionTypes.SET_FIRST_LOGIN:
      return {
        ...state,
        isFirstLogin: action.payload,
      };
    default:
      return state;
  }
};

